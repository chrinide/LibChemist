size_t most_common_isotope(size_t Z){
  const auto& ad=atomic_data_.at(Z);
  auto maxit = std::max_element(
      ad.isotopes.begin(), ad.isotopes.end(),
      [](const IsotopeData & idat1, const IsotopeData & idat2)
      { return idat1.abund < idat2.abund; });
  return maxit->isonum;
}

double isotope_mass(size_t Z, size_t isonum){
  const auto& ad=atomic_data_.at(Z);
  for(const auto& x: ad.isotopes)
     if(x.isonum==isonum)return x.mass;
   throw std::out_of_range("Isotope number is not in range");
}

